//
// 文字列 S の相異なる subsequence の個数を求める (by 集める DP, in O(N))
//
// verified:
//   Yosupo Library Checker - Number of Subsequences
//     https://judge.yosupo.jp/problem/number_of_subsequences
//


#include <bits/stdc++.h>
using namespace std;


// 文字列 S の相異なる subsequence の個数
template<class mint, class Str> mint calc_num_subsequences(const Str &S) {
    int N = (int)S.size();
    unordered_map<long long, vector<long long>> mp;
    for (int i = 0; i < N; i++) mp[S[i]].emplace_back(i);
    vector<int> prev(N, -1);
    for (auto [val, v] : mp) for (int i = 1; i < v.size(); i++) prev[v[i]] = v[i-1];
    vector<mint> dp(N+1, 0), sdp(N+2, 0);
    dp[0] = 1, sdp[1] = 1;
    for (int i = 0; i < N; i++) {
        long long l = prev[i] + 1;
        dp[i+1] = sdp[i+1] - sdp[l];
        sdp[i+2] = sdp[i+1] + dp[i+1];
    }
    return sdp.back() - 1;
}


